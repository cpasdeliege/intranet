package be.cpasdeliege.intranet.informatique.model;

public class Statistiques {
	int ordinateurs;
	int personnels;
	int services;
	int imprimantes;
	int fonctions;

	public int getOrdinateurs() {
		return ordinateurs;
	}

	public void setOrdinateurs(int ordinateurs) {
		this.ordinateurs = ordinateurs;
	}

	public int getPersonnels() {
		return personnels;
	}

	public void setPersonnels(int personnels) {
		this.personnels = personnels;
	}

	public int getServices() {
		return services;
	}

	public void setServices(int services) {
		this.services = services;
	}

	public int getImprimantes() {
		return imprimantes;
	}

	public void setImprimantes(int imprimantes) {
		this.imprimantes = imprimantes;
	}

	public int getFonctions() {
		return fonctions;
	}

	public void setFonctions(int fonctions) {
		this.fonctions = fonctions;
	}

}
