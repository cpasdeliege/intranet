package be.cpasdeliege.intranet.codesRues.model.dao;

public class Addenda {
	String abrevRue = "";
	String typeRue = "";
	String nomRue = "";
	String antenne = "";

	public String getAbrevRue() {
		return abrevRue;
	}

	public void setAbrevRue(String abrevRue) {
		this.abrevRue = abrevRue;
	}

	public String getTypeRue() {
		return typeRue;
	}

	public void setTypeRue(String typeRue) {
		this.typeRue = typeRue;
	}

	public String getNomRue() {
		return nomRue;
	}

	public void setNomRue(String nomRue) {
		this.nomRue = nomRue;
	}

	public String getAntenne() {
		return antenne;
	}

	public void setAntenne(String antenne) {
		this.antenne = antenne;
	}
}
