package be.cpasdeliege.intranet.formations.model;

public class Formation {
	int idFormation = 0;
	String intitule = "";
	String objectif = "";
	String synthese = "";
	String methodologie = "";
	String operateur = "";
	String personneVisee = "";
	String rapportFormation = "";
	String typologie = "";
	String departement = "";
	String service = "";
	String duree = "";
	String lieu = "";
	String annee = "";
	String rapport = "";

	public int getIdFormation() {
		return idFormation;
	}

	public void setIdFormation(int idFormation) {
		this.idFormation = idFormation;
	}

	public String getIntitule() {
		return intitule;
	}

	public void setIntitule(String intitule) {
		this.intitule = intitule;
	}

	public String getObjectif() {
		return objectif;
	}

	public void setObjectif(String objectif) {
		this.objectif = objectif;
	}

	public String getSynthese() {
		return synthese;
	}

	public void setSynthese(String synthese) {
		this.synthese = synthese;
	}

	public String getMethodologie() {
		return methodologie;
	}

	public void setMethodologie(String methodologie) {
		this.methodologie = methodologie;
	}

	public String getOperateur() {
		return operateur;
	}

	public void setOperateur(String operateur) {
		this.operateur = operateur;
	}

	public String getPersonneVisee() {
		return personneVisee;
	}

	public void setPersonneVisee(String personneVisee) {
		this.personneVisee = personneVisee;
	}

	public String getRapportFormation() {
		return rapportFormation;
	}

	public void setRapportFormation(String rapportFormation) {
		this.rapportFormation = rapportFormation;
	}

	public String getTypologie() {
		return typologie;
	}

	public void setTypologie(String typologie) {
		this.typologie = typologie;
	}

	public String getDepartement() {
		return departement;
	}

	public void setDepartement(String departement) {
		this.departement = departement;
	}

	public String getService() {
		return service;
	}

	public void setService(String service) {
		this.service = service;
	}

	public String getDuree() {
		return duree;
	}

	public void setDuree(String duree) {
		this.duree = duree;
	}

	public String getLieu() {
		return lieu;
	}

	public void setLieu(String lieu) {
		this.lieu = lieu;
	}

	public String getAnnee() {
		return annee;
	}

	public void setAnnee(String annee) {
		this.annee = annee;
	}

	public String getRapport() {
		return rapport;
	}

	public void setRapport(String rapport) {
		this.rapport = rapport;
	}

}
