package be.cpasdeliege.intranet.DemServInfo.model;

public class StatDem {
	String nbrAttenteDG = "";
	String nbrAttenteInfo = "";
	String nbrAcceptee = "";
	String nbrAnneeEnCours = "";
	String annee = "";

	public String getNbrAttenteDG() {
		return nbrAttenteDG;
	}

	public void setNbrAttenteDG(String nbrAttenteDG) {
		this.nbrAttenteDG = nbrAttenteDG;
	}

	public String getNbrAttenteInfo() {
		return nbrAttenteInfo;
	}

	public void setNbrAttenteInfo(String nbrAttenteInfo) {
		this.nbrAttenteInfo = nbrAttenteInfo;
	}

	public String getNbrAcceptee() {
		return nbrAcceptee;
	}

	public void setNbrAcceptee(String nbrAcceptee) {
		this.nbrAcceptee = nbrAcceptee;
	}

	public String getNbrAnneeEnCours() {
		return nbrAnneeEnCours;
	}

	public void setNbrAnneeEnCours(String nbrAnneeEnCours) {
		this.nbrAnneeEnCours = nbrAnneeEnCours;
	}

	public String getAnnee() {
		return annee;
	}

	public void setAnnee(String annee) {
		this.annee = annee;
	}
}
