package be.cpasdeliege.intranet.informatique.model;

public class StatistiquesAnnuaire {
	int pagesTotal;
	int ordinateursTotal;
	int pagesHier;
	int ordinateursHier;
	int pagesAujourdhui;
	int ordinateursAujourdhui;

	public int getPagesTotal() {
		return pagesTotal;
	}

	public void setPagesTotal(int pagesTotal) {
		this.pagesTotal = pagesTotal;
	}

	public int getOrdinateursTotal() {
		return ordinateursTotal;
	}

	public void setOrdinateursTotal(int ordinateursTotal) {
		this.ordinateursTotal = ordinateursTotal;
	}

	public int getPagesHier() {
		return pagesHier;
	}

	public void setPagesHier(int pagesHier) {
		this.pagesHier = pagesHier;
	}

	public int getOrdinateursHier() {
		return ordinateursHier;
	}

	public void setOrdinateursHier(int ordinateursHier) {
		this.ordinateursHier = ordinateursHier;
	}

	public int getPagesAujourdhui() {
		return pagesAujourdhui;
	}

	public void setPagesAujourdhui(int pagesAujourdhui) {
		this.pagesAujourdhui = pagesAujourdhui;
	}

	public int getOrdinateursAujourdhui() {
		return ordinateursAujourdhui;
	}

	public void setOrdinateursAujourdhui(int ordinateursAujourdhui) {
		this.ordinateursAujourdhui = ordinateursAujourdhui;
	}

}
